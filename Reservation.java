    public void Load()
   {
       String splace=txtStartingPlace.getSelectedItem().toString();
       String eplace=txtDestinationPlace.getSelectedItem().toString();
       //String seatNumber=txtSeatBooking.getSelectedItem().toString();
       try
       {
            pst=con.prepareStatement("SELECT  *FROM traindetails where startplace = ? and destinationplace = ?");
           // pst=con.prepareStatement("SELECT  *FROM traindetails where startplace = ? and destinationplace = ?");

            pst.setString(1, splace);
            pst.setString(2, eplace);
            rs= pst.executeQuery();
            //txteplace.removeAllItems();
            
            if(rs.next()==true)
            {
                txtTrainNumber.setText(rs.getString(1));
                txtTrainName.setText(rs.getString(2));
                txtPerTicket.setText(rs.getString(5));
            }
            else 
            {
                  JOptionPane.showMessageDialog(this,"Record not found");
            }
       }
       catch(SQLException e)
       {
       }
       
       
   }
    
    
