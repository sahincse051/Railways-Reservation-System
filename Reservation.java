    public void LoadStartplace()
   {
       try
       {
            pst=con.prepareStatement("SELECT startplace FROM traindetails");
            rs= pst.executeQuery();
            txtStartingPlace.removeAllItems();
            
            while(rs.next())
            {
                txtStartingPlace.addItem(rs.getString(1));
            }
       }
       catch(SQLException e)
       {
       }
       
       
   }
   
   
   
