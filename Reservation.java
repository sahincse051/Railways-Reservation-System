    private void txtNumOfTicketKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumOfTicketKeyPressed
        // TODO add your handling code here:

        int price,ticket,tot;

        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            price=Integer.parseInt(txtPerTicket.getText());
            ticket=Integer.parseInt(txtNumOfTicket.getText());
            tot=price*ticket;

            txtTotalPrice.setText(String.valueOf(tot));

        }
    }//GEN-LAST:event_txtNumOfTicketKeyPressed

    private void BtnReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnReserveActionPerformed
        // TODO add your handling code here:

        String pno=txtPNumber.getText();
        String splace=txtStartingPlace.getSelectedItem().toString();
        String eplace=txtDestinationPlace.getSelectedItem().toString();
        String tno=txtTrainNumber.getText();
        String tname=txtTrainName.getText();
        String price=txtPerTicket.getText();
        String seat=txtSeatBooking.getSelectedItem().toString();
        String date=txtDate.getText();
        String ticket=txtNumOfTicket.getText();
        String total=txtTotalPrice.getText();
        

        try {
            pst= con.prepareStatement("insert into reservation(pno,splace,eplace,tno,tname,price,date,ticket,tot,seat)values(?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, pno);
            pst.setString(2, splace);
            pst.setString(3, eplace);
            pst.setString(4, tno);
            pst.setString(5, tname);
            pst.setString(6, price);
            pst.setString(7, date);
            pst.setString(8, ticket);
            pst.setString(9, total);
             pst.setString(10, seat);

            int k=pst.executeUpdate();
            if(k==1)
            {
                JOptionPane.showMessageDialog(this,"Ticket Confirm successfuly");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Ticket Purchase Failed");

            }

        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_BtnReserveActionPerformed

